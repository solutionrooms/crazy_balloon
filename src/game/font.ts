const GW = 3, GH = 5;

/** Draw text at (x,y) top-left, `scale` px per font pixel, `gap` px between chars. */
export function drawText(
  ctx: CanvasRenderingContext2D, x: number, y: number, text: string,
  color: string, scale = 1, gap = 1,
): void {
  ctx.fillStyle = color;
  let cx = x;
  for (const ch of text.toUpperCase()) {
    const g = GLYPHS[ch] ?? GLYPHS[" "];
    for (let r = 0; r < GH; r++) {
      for (let c = 0; c < GW; c++) {
        if (g[r][c] === "#") ctx.fillRect(cx + c * scale, y + r * scale, scale, scale);
      }
    }
    cx += (GW + gap) * scale;
  }
}

/** Pixel width of a string at the given scale. */
export function textWidth(text: string, scale = 1, gap = 1): number {
  return text.length * (GW + gap) * scale - gap * scale;
}
